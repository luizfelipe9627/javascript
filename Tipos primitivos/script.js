/*
  Os tipos de dados primitivos são os tipos básicos que devem ser implementados por todas as linguagens de programação, como os números reais, inteiros, booleanos, caracteres e strings.
*/

console.log('=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=');

// Variável com um tipo indefinido(undefined).
let ind; 
console.log(`Olá, eu sou do tipo:`, typeof ind);

// Variável do tipo number que trabalha com números.
let num = 10; 
console.log(`Olá, eu sou do tipo:`, typeof num);

// Variável do tipo char que trabalha com palavras e caracteres.
let nome = 'Luiz'; 
console.log(`Olá, eu sou do tipo:`, typeof nome);

// Variável do tipo boolean que trabalhar com verdade ou falso. 
//true = 1 e o false = 0;
let bool = true; 
console.log(`Olá, eu sou do tipo:`, typeof bool);

console.log('=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=');