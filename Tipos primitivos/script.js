let ind; // Variável com um tipo indefinido(undefined).
console.log(`Olá, eu sou do tipo:`, typeof ind);

let num = 10; // Variável do tipo number que trabalha com números.
console.log(`Olá, eu sou do tipo:`, typeof num);

let nome = 'Luiz'; // Variável do tipo char que trabalha com palavras e caracteres.
console.log(`Olá, eu sou do tipo:`, typeof nome);

let bool = true; // Variável do tipo boolean que trabalhar com verdade ou falso. 
//true = 1 e o false = 0;
console.log(`Olá, eu sou do tipo:`, typeof bool);